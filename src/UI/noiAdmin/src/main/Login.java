    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String getAdminAccount = adminAccount.getText().trim();
        String getAdminPassword = new String(password.getPassword()).trim();

        if(getAdminAccount.isEmpty() || getAdminPassword.isEmpty()){
            JOptionPane.showMessageDialog(this, "Please fill in all data field!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        boolean authen = Admin.checkAdminLogin(getAdminAccount, getAdminPassword);

        if (authen) {
            
            JFrame frame = new JFrame("Admin");
            frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            frame.setSize(1400, 750);

            // Sử dụng NoiAdmin để tạo panel
            JPanel adminPanel = mainFrame.mainAdmin(frame);

            frame.add(adminPanel);
            frame.setLocationRelativeTo(null);
            
            //Quan trong
            Planes.updatePlaneBaseOnFlight();
            frame.setVisible(true);
        }
        else{
            JOptionPane.showMessageDialog(this, "Admin account or password is wrong!", "Failed", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

