    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Lấy giá trị từ các ô nhập liệu
        String planeID = FlightID.getText().trim();
        String departureCity = Departure.getText().trim();
        String arrivalCity = Arrival.getText().trim();

        // Kiểm tra nếu người dùng không nhập gì
        if (planeID.isEmpty() && departureCity.isEmpty() && arrivalCity.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter at least one search criteria.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Gọi phương thức viewFlight với các tham số
        ArrayList<ArrayList<Object>> flights = Flights.viewFlight("", "", planeID, departureCity, arrivalCity);

        // Hiển thị kết quả
        if (flights.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No flights found.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Tạo JTextArea để hiển thị kết quả
            JTextArea resultArea = new JTextArea(10, 50); // 10 dòng, 50 cột
            resultArea.setEditable(false); // Không cho chỉnh sửa
            StringBuilder result = new StringBuilder("Search Results:\n");
            for (ArrayList<Object> flight : flights) {
                result.append("FlightID: ").append(flight.get(0))
                        .append(", Departure: ").append(flight.get(1))
                        .append(", Arrival: ").append(flight.get(2))
                        .append(", PlaneID: ").append(flight.get(3))
                        .append(", From: ").append(flight.get(6))
                        .append(", To: ").append(flight.get(7))
                        .append("\n");
            }
            resultArea.setText(result.toString());
            resultArea.setCaretPosition(0);
            // Đặt JTextArea vào JScrollPane
            JScrollPane scrollPane = new JScrollPane(resultArea);

            // Hiển thị trong JOptionPane
            JOptionPane.showMessageDialog(this, scrollPane, "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

