    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String getAirportID = airportID.getText().trim();
       
        if (getAirportID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Fill the airport ID you want to delete", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        boolean success = Airports.deleteAirport(Integer.parseInt(getAirportID));
        if (success) {
            JOptionPane.showMessageDialog(this, "Delete airport with ID: " + getAirportID + " successful!");
        } else {
            JOptionPane.showMessageDialog(this, "Please check the ID again", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

