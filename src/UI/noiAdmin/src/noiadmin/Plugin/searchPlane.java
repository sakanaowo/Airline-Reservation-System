    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String modelI = model.getText().trim();
        String airport = model1.getText().trim();
        String updatedBy = model2.getText().trim();

        if (modelI.isEmpty() && airport.isEmpty() && updatedBy.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter at least one search criteria.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        ArrayList<ArrayList<Object>> planes = Planes.viewPlane(modelI, airport, updatedBy);

        if (planes.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No planes found.", "Search Result", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JTextArea resultArea = new JTextArea(10, 50); 
            resultArea.setEditable(false); 

            StringBuilder result = new StringBuilder("Search Results:\n");

            for (ArrayList<Object> plane : planes) {
                result.append("Plane Model: ").append(plane.get(0))
                        .append(", Airport: ").append(plane.get(1))
                        .append(", Updated By: ").append(plane.get(2))
                        .append(", Date: ").append(plane.get(3))
                        .append("\n");
            }

            resultArea.setText(result.toString());
            resultArea.setCaretPosition(0); 

            JScrollPane scrollPane = new JScrollPane(resultArea);

            JOptionPane.showMessageDialog(this, scrollPane, "Search Result", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

